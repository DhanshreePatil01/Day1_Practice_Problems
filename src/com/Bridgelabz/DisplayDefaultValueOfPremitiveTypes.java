package com.Bridgelabz;

public class DisplayDefaultValueOfPremitiveTypes {

	
	static byte num1;
	static short num2;
	static int num3;
	static long num4;
	static float num5;
	static double num6;
	static char num7;
	static boolean num8;
	
	public static void main(String[] args) {
		
		System.out.println("------------------Default Value of all premitive data types: ------------------------\n");
		
		System.out.println("  Default Value Of Byte: "+num1);
		
		System.out.println("  Default Value Of short: "+num2);
		
		System.out.println("  Default Value Of int: "+num3);
		
		System.out.println("  Default Value Of long: "+num4);
		
		System.out.println("  Default Value Of float: "+num5);
		
		System.out.println("  Default Value Of double: "+num6);
		
		System.out.println("  Default Value Of char: "+num7);
		
		System.out.println("  Default Value Of boolean: "+num8);
		
		System.out.println("--------------------------------------------------------------------------------------\n");
	}
}
